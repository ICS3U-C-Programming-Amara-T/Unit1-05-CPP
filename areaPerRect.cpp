// This program demonstrates how to calculate the area and perimeter in C++
//  with the dimensions of 2cm x 7cm

#include <iostream>
#include <cmath>

int main() {
    std::cout << "If the rectangle has the dimensions" << std::endl;
    std::cout << "2cm x 7cm"  << std::endl;
    std::cout << ""  << std::endl;
    std::cout << "The Area is:" << (2*7) << "cm^2" << std::endl;
    std::cout << "The Perimeter is:" << (2*(5+3)) << "cm" << std::endl;

    return 0;
}
